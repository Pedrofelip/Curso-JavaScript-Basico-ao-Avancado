//O laço for  é um dos laços de repetição mais utilizados dentro do mundo da programação, ele é uma forma mais compacta de se fazer um laço e é utilizado quando já sabemos quantas vzs vamos querer que o loop aconteça, pois declaramos a variavel contadora dentro dele.

for (let i = 0; i < 5; i++) {
    console.log(i)
}

const array = ["pedro", "gabi", "mari", "mafalda"]

for (let i = 0; i < array.length; i++) {
    console.log(array[i])
}